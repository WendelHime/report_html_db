CREATE TABLE TEXTS (
	id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL, 
	tag VARCHAR(200),
	value VARCHAR(2000),
	details VARCHAR(2000)
);

CREATE TABLE IMAGES (
	id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
	tag VARCHAR(200),
	filepath VARCHAR(2000),
	details VARCHAR(2000)
);

CREATE TABLE RELATIONS_TEXTS_IMAGES(
	id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
	idImage INTEGER,
	idText INTEGER,
	FOREIGN KEY(idImage) REFERENCES IMAGES(id),
	FOREIGN KEY(idText) REFERENCES TEXTS(id)
);

